package cn.ac.ucas.model;

public class Elective {
	private int id;
	private String cname;
	private int score;
	
	public int getId(){
		return id;
	}
	public void setId(){
		this.id=id;
	}
	public String getCname(){
		return cname;	
	}
	public void setCname(){
		this.cname=cname;
	}
	public int getScore(){
		return score;
	}
	public void setScore(){
		this.score=score;
	}

}
